package model;

import java.util.Date;

public class SearchCaseModel {
	private String firstName;
	private String lastName;
	private long crnumber;
	private Date plannedProcedureDateTime;
	private String status;
	private Long caseId;
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public long getCrnumber() {
		return crnumber;
	}
	public void setCrnumber(long crnumber) {
		this.crnumber = crnumber;
	}
	public Date getPlannedProcedureDateTime() {
		return plannedProcedureDateTime;
	}
	public void setPlannedProcedureDateTime(Date plannedProcedureDateTime) {
		this.plannedProcedureDateTime = plannedProcedureDateTime;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public Long getCaseId() {
		return caseId;
	}
	public void setCaseId(Long caseId) {
		this.caseId = caseId;
	}



}
