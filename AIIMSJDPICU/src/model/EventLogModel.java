package model;

import java.util.Date;

public class EventLogModel {

	private Integer eventLogId;
	private Integer eventId;
	private String eventname;
	private String type;
	private String details;
	private Date date;
	private Date eventTime;
	private Boolean status;
	private String isExecuted;
	public EventLogModel(Integer eventLogId, Integer eventId, String eventname, String type, String details, Date date,
			Date eventTime, Boolean status, String isExecuted) {
		super();
		this.eventLogId = eventLogId;
		this.eventId = eventId;
		this.eventname = eventname;
		this.type = type;
		this.details = details;
		this.date = date;
		this.eventTime = eventTime;
		this.status = status;
		this.isExecuted = isExecuted;
	}
	public EventLogModel(){

	}
	public Integer getEventLogId() {
		return eventLogId;
	}
	public void setEventLogId(Integer eventLogId) {
		this.eventLogId = eventLogId;
	}
	public Integer getEventId() {
		return eventId;
	}
	public void setEventId(Integer eventId) {
		this.eventId = eventId;
	}
	public String getEventname() {
		return eventname;
	}
	public void setEventname(String eventname) {
		this.eventname = eventname;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDetails() {
		return details;
	}
	public void setDetails(String details) {
		this.details = details;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Date getEventTime() {
		return eventTime;
	}
	public void setEventTime(Date eventTime) {
		this.eventTime = eventTime;
	}
	public Boolean getStatus() {
		return status;
	}
	public void setStatus(Boolean status) {
		this.status = status;
	}
	public String getIsExecuted() {
		return isExecuted;
	}
	public void setIsExecuted(String isExecuted) {
		this.isExecuted = isExecuted;
	}


}
