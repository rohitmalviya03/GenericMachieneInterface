package model;

public class DeviceModel {
	private long deviceCongigurationId;
	private String departmentName;
	private String model;
	private String serialNumber;
	private String medicine;
	private String wardNumber;
	private String otName;
	private String status;
	private boolean deviceStatus;
	private String portNumber;

	public DeviceModel(){

	}
	public DeviceModel(int deviceCongigurationId, String departmentName, String model, String serialNumber,
			String medicine, String wardNumber, String otName, String status, boolean deviceStatus, String portNumber) {
		super();
		this.deviceCongigurationId = deviceCongigurationId;
		this.departmentName = departmentName;
		this.model = model;
		this.serialNumber = serialNumber;
		this.medicine = medicine;
		this.wardNumber = wardNumber;
		this.otName = otName;
		this.status = status;
		this.deviceStatus = deviceStatus;
		this.portNumber = portNumber;
	}
	public long getDeviceCongigurationId() {
		return deviceCongigurationId;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public String getModel() {
		return model;
	}
	public String getSerialNumber() {
		return serialNumber;
	}
	public String getMedicine() {
		return medicine;
	}
	public String getWardNumber() {
		return wardNumber;
	}
	public String getOtName() {
		return otName;
	}
	public String getStatus() {
		return status;
	}
	public boolean isDeviceStatus() {
		return deviceStatus;
	}
	public String getPortNumber() {
		return portNumber;
	}
	public void setDeviceCongigurationId(long deviceCongigurationId) {
		this.deviceCongigurationId = deviceCongigurationId;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public void setMedicine(String medicine) {
		this.medicine = medicine;
	}
	public void setWardNumber(String wardNumber) {
		this.wardNumber = wardNumber;
	}
	public void setOtName(String otName) {
		this.otName = otName;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public void setDeviceStatus(boolean deviceStatus) {
		this.deviceStatus = deviceStatus;
	}
	public void setPortNumber(String portNumber) {
		this.portNumber = portNumber;
	}


}
